$(function() {
  $("#data_text").on("keydown keyup keypress change", function() {
    if (isUrlValid($(this).val())) {
      $(".search_btn").prop("disabled", false);
    } else {
      $(".search_btn").prop("disabled", true);
    }
  });
});
